
public class PrintfMethod {
	public static void main(String[] args) {
		boolean myBoolean = true;
		char myChar = '@';
		String myString = "Marcus";
		int myInt = 50;
		double myDouble = 3.14;
		
		
		System.out.printf("This is a formated string %b", myBoolean);
		System.out.printf("This is a formated string %c", myChar);
		System.out.printf("This is a formated string %d", myInt);
		System.out.printf("This is a formated string %s", myString);
		System.out.printf("This is a formated string %f", myDouble);
		  
		
		
	}
}
	